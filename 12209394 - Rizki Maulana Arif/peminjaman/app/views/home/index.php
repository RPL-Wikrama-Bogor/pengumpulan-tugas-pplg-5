   <p>Waktu Peminjaman: Barang hanya dapat dipinjamkan selama waktu yang telah disepakati sebelumnya. Pastikan untuk mengembalikannya tepat waktu sesuai dengan yang telah disepakati.

Kondisi Barang: Barang yang dipinjam harus dijaga dengan baik dan dikembalikan dalam kondisi yang sama saat dipinjamkan. Jika terjadi kerusakan, peminjam harus bertanggung jawab untuk memperbaikinya atau menggantinya.

Peminjam yang Bertanggung Jawab: Peminjam adalah satu-satunya yang bertanggung jawab atas barang yang dipinjam. Tidak diperbolehkan untuk meminjamkan atau mentransfer barang tersebut kepada orang lain tanpa izin dari pemiliknya.</p>
